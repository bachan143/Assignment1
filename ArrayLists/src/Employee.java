/*This class set the values to the datamember and get the datamember using get and set method
*/public class Employee 
{
	private String name;
	private double id;
	private double salary;
	public String getName() 
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public double getId() {
		return id;
	}
	public void setId(double id) 
	{
		this.id = id;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	
	
	
}
